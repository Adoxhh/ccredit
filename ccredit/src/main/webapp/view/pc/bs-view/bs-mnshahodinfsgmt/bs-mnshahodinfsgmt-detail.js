var bsMnshahodinfsgmtWinDetail;
var bsMnshahodinfsgmtFormDetail;
function detailBsMnshahodinfsgmt(){
	var record = grid.getSelectionModel().selected;
	if(record.length == 0){
		msgTishi('请选择要查看明细的项！');
		return;
	}
	initBsMnshahodinfsgmtFormDetail();
	
	var selected=record.items[0].data.sharhodidtype2;
	if(selected=='1'){	         
		Ext.getCmp('sharhodidtype2').setStore(IDTYPECODE);
	}
	if(selected=='2'){	         
		Ext.getCmp('sharhodidtype2').setStore(OTHENTCERTTYPECODE);
	}
	
	bsMnshahodinfsgmtWinDetail = Ext.create('Ext.Window',{
		layout:'fit',
		width:800,
		height:400,
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'明细信息',
		listeners:{
			minimize:function(win,opts){
				if(!win.collapse()){
					win.collapse();
				}else{
					win.expand();
				}
			}
		},
		items:bsMnshahodinfsgmtFormDetail,
		buttons:[{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	bsMnshahodinfsgmtWinDetail.show();
	
	loadFormData(bsMnshahodinfsgmtFormDetail,'../bsMnshahodinfsgmtController/getBsMnshahodinfsgmtById?bs_mnshahodinfsgmt_id='+ record.items[0].data.bs_mnshahodinfsgmt_id);
}
