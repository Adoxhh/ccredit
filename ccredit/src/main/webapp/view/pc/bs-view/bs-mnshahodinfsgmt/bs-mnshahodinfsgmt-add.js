var bsMnshahodinfsgmtWinAdd;
var bsMnshahodinfsgmtFormAdd;
function addBsMnshahodinfsgmt(){
	initBsMnshahodinfsgmtFormAdd();
	bsMnshahodinfsgmtWinAdd = Ext.create('Ext.Window',{
		layout:'fit',
		width:800,
		height:400,
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'添加信息',
		listeners:{
			minimize:function(win,opts){
				if(!win.collapse()){
					win.collapse();
				}else{
					win.expand();
				}
			}
		},
		items:bsMnshahodinfsgmtFormAdd,
		buttons:[{
			text:'保存',
			itemId:'save',
			handler:function(button){
				submitForm(bsMnshahodinfsgmtFormAdd,'../bsMnshahodinfsgmtController/addBsMnshahodinfsgmt',grid,bsMnshahodinfsgmtWinAdd,false,true);
			}
		},{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	bsMnshahodinfsgmtWinAdd.show();
	
}
