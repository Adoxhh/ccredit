var bsIdsgmtWinDetail;
var bsIdsgmtFormDetail;
function detailBsIdsgmt(){
	var record = grid.getSelectionModel().selected;
	if(record.length == 0){
		msgTishi('请选择要查看明细的项！');
		return;
	}
	initBsIdsgmtFormDetail();
	bsIdsgmtWinDetail = Ext.create('Ext.Window',{
		layout:'fit',
		width:800,
		height:400,
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'明细信息',
		listeners:{
			minimize:function(win,opts){
				if(!win.collapse()){
					win.collapse();
				}else{
					win.expand();
				}
			}
		},
		items:bsIdsgmtFormDetail,
		buttons:[{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	bsIdsgmtWinDetail.show();
	
	loadFormData(bsIdsgmtFormDetail,'../bsIdsgmtController/getBsIdsgmtById?bs_idsgmt_id='+ record.items[0].data.bs_idsgmt_id);
}
