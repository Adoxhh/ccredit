var bsIdsgmtWinEdit;
var bsIdsgmtFormEdit;
function updateBsIdsgmt(){
	var record = grid.getSelectionModel().selected;
	if(record.length == 0){
		msgTishi('请选择要修改的一项！');
		return;
	}
	initBsIdsgmtFormEdit();
	bsIdsgmtWinEdit = Ext.create('Ext.Window',{
		layout:'fit',
		width:800,
		height:400,
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'编辑信息',
		listeners:{
			minimize:function(win,opts){
				if(!win.collapse()){
					win.collapse();
				}else{
					win.expand();
				}
			}
		},
		items:bsIdsgmtFormEdit,
		buttons:[{
			text:'保存',
			itemId:'save',
			handler:function(button){
				submitForm(bsIdsgmtFormEdit,'../bsIdsgmtController/updateBsIdsgmt',grid,bsIdsgmtWinEdit,false,true);
			}
		},{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	bsIdsgmtWinEdit.show();
	
	loadFormData(bsIdsgmtFormEdit,'../bsIdsgmtController/getBsIdsgmtById?bs_idsgmt_id='+ record.items[0].data.bs_idsgmt_id);
}
