var bsActucotrlinfsgmtWinEdit;
var bsActucotrlinfsgmtFormEdit;
function updateBsActucotrlinfsgmt(){
	var record = grid.getSelectionModel().selected;
	if(record.length == 0){
		msgTishi('请选择要修改的一项！');
		return;
	}
	initBsActucotrlinfsgmtFormEdit();
	
	var selected=record.items[0].data.actucotrlidtype1;
	if(selected=='1'){	         
		Ext.getCmp('actucotrlidtype2').setStore(IDTYPECODE);
	}
	if(selected=='2'){	         
		Ext.getCmp('actucotrlidtype2').setStore(OTHENTCERTTYPECODE);
	}
	bsActucotrlinfsgmtWinEdit = Ext.create('Ext.Window',{
		layout:'fit',
		width:800,
		height:400,
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'编辑信息',
		listeners:{
			minimize:function(win,opts){
				if(!win.collapse()){
					win.collapse();
				}else{
					win.expand();
				}
			}
		},
		items:bsActucotrlinfsgmtFormEdit,
		buttons:[{
			text:'保存',
			itemId:'save',
			handler:function(button){
				submitForm(bsActucotrlinfsgmtFormEdit,'../bsActucotrlinfsgmtController/updateBsActucotrlinfsgmt',grid,bsActucotrlinfsgmtWinEdit,false,true);
			}
		},{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	bsActucotrlinfsgmtWinEdit.show();
	
	loadFormData(bsActucotrlinfsgmtFormEdit,'../bsActucotrlinfsgmtController/getBsActucotrlinfsgmtById?bs_actucotrlinfsgmt_id='+ record.items[0].data.bs_actucotrlinfsgmt_id);
}
