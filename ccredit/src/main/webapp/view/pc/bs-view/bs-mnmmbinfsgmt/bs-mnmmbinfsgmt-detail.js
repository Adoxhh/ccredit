var bsMnmmbinfsgmtWinDetail;
var bsMnmmbinfsgmtFormDetail;
function detailBsMnmmbinfsgmt(){
	var record = grid.getSelectionModel().selected;
	if(record.length == 0){
		msgTishi('请选择要查看明细的项！');
		return;
	}
	initBsMnmmbinfsgmtFormDetail();
	bsMnmmbinfsgmtWinDetail = Ext.create('Ext.Window',{
		layout:'fit',
		width:800,
		height:400,
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'明细信息',
		listeners:{
			minimize:function(win,opts){
				if(!win.collapse()){
					win.collapse();
				}else{
					win.expand();
				}
			}
		},
		items:bsMnmmbinfsgmtFormDetail,
		buttons:[{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	bsMnmmbinfsgmtWinDetail.show();
	
	loadFormData(bsMnmmbinfsgmtFormDetail,'../bsMnmmbinfsgmtController/getBsMnmmbinfsgmtById?bs_mnmmbinfsgmt_id='+ record.items[0].data.bs_mnmmbinfsgmt_id);
}
