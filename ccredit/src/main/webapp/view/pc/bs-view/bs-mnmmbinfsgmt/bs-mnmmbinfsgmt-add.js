var bsMnmmbinfsgmtWinAdd;
var bsMnmmbinfsgmtFormAdd;
function addBsMnmmbinfsgmt(){
	initBsMnmmbinfsgmtFormAdd();
	bsMnmmbinfsgmtWinAdd = Ext.create('Ext.Window',{
		layout:'fit',
		width:800,
		height:400,
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'添加信息',
		listeners:{
			minimize:function(win,opts){
				if(!win.collapse()){
					win.collapse();
				}else{
					win.expand();
				}
			}
		},
		items:bsMnmmbinfsgmtFormAdd,
		buttons:[{
			text:'保存',
			itemId:'save',
			handler:function(button){
				submitForm(bsMnmmbinfsgmtFormAdd,'../bsMnmmbinfsgmtController/addBsMnmmbinfsgmt',grid,bsMnmmbinfsgmtWinAdd,false,true);
			}
		},{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	bsMnmmbinfsgmtWinAdd.show();
	
}
