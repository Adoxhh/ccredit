function updateFin2002balancesheetsgmt(){
	var record = grid.getSelectionModel().selected;
	if(record.length == 0){
		msgTishi('请选择要修改的一项！');
		return;
	}
	initFin2002balancesheetsgmtFormEdit();
	fin2002balancesheetsgmtWinEdit = Ext.create('Ext.Window',{
		layout:'fit',
		width:800,
		height:400,
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'编辑信息',
		listeners:{
			minimize:function(win,opts){
				if(!win.collapse()){
					win.collapse();
				}else{
					win.expand();
				}
			}
		},
		items:fin2002balancesheetsgmtFormEdit,
		buttons:[{
			text:'保存',
			itemId:'save',
			handler:function(button){
				submitForm(fin2002balancesheetsgmtFormEdit,'../fin2002balancesheetsgmtController/updateFin2002balancesheetsgmt',grid,fin2002balancesheetsgmtWinEdit,false,true);
			}
		},{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	fin2002balancesheetsgmtWinEdit.show();
	
	loadFormData(fin2002balancesheetsgmtFormEdit,'../fin2002balancesheetsgmtController/getFin2002balancesheetsgmtById?fin_2002balancesheetsgmt_id='+ record.items[0].data.fin_2002balancesheetsgmt_id);
}
