function detailFinInstitutionbalancesheetsgmt(){
	var record = grid.getSelectionModel().selected;
	if(record.length == 0){
		msgTishi('请选择要查看明细的项！');
		return;
	}
	initFinInstitutionbalancesheetsgmtFormDetail();
	finInstitutionbalancesheetsgmtWinDetail = Ext.create('Ext.Window',{
		layout:'fit',
		width:800,
		height:400,
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'明细信息',
		listeners:{
			minimize:function(win,opts){
				if(!win.collapse()){
					win.collapse();
				}else{
					win.expand();
				}
			}
		},
		items:finInstitutionbalancesheetsgmtFormDetail,
		buttons:[{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	finInstitutionbalancesheetsgmtWinDetail.show();
	
	loadFormData(finInstitutionbalancesheetsgmtFormDetail,'../finInstitutionbalancesheetsgmtController/getFinInstitutionbalancesheetsgmtById?fin_institutionbalancesheetsgmt_id='+ record.items[0].data.fin_institutionbalancesheetsgmt_id);
}
