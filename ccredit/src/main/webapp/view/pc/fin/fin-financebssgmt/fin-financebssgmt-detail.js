function detailFinFinancebssgmt(){
	var record = grid.getSelectionModel().selected;
	if(record.length == 0){
		msgTishi('请选择要查看明细的项！');
		return;
	}
	initFinFinancebssgmtFormDetail();
	finFinancebssgmtWinDetail = Ext.create('Ext.Window',{
		layout:'fit',
		width:800,
		height:400,
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'明细信息',
		listeners:{
			minimize:function(win,opts){
				if(!win.collapse()){
					win.collapse();
				}else{
					win.expand();
				}
			}
		},
		items:finFinancebssgmtFormDetail,
		buttons:[{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	finFinancebssgmtWinDetail.show();
	
	loadFormData(finFinancebssgmtFormDetail,'../finFinancebssgmtController/getFinFinancebssgmtById?fin_financebssgmt_id='+ record.items[0].data.fin_financebssgmt_id);
}
