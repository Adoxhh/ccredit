	if(record.length == 0){
		msgTishi('请选择要修改的一项！');
		return;
	}
	initLoanCtrctcertrelsgmtFormEdit();
	loanCtrctcertrelsgmtWinEdit = Ext.create('Ext.Window',{
		layout:'fit',
		width:800,
		height:400,
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'编辑信息',
		listeners:{
			minimize:function(win,opts){
				if(!win.collapse()){
					win.collapse();
				}else{
					win.expand();
				}
			}
		},
		items:loanCtrctcertrelsgmtFormEdit,
		buttons:[{
			text:'保存',
			itemId:'save',
			handler:function(button){
				submitForm(loanCtrctcertrelsgmtFormEdit,'../loanCtrctcertrelsgmtController/updateLoanCtrctcertrelsgmt',grid,loanCtrctcertrelsgmtWinEdit,false,true);
			}
		},{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	loanCtrctcertrelsgmtWinEdit.show();
	
	loadFormData(loanCtrctcertrelsgmtFormEdit,'../loanCtrctcertrelsgmtController/getLoanCtrctcertrelsgmtById?loan_ctrctcertrelsgmt_id='+ record.items[0].data.loan_ctrctcertrelsgmt_id);
