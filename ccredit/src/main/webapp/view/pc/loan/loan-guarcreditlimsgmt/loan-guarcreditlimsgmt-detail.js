function detailLoanGuarcreditlimsgmt(){
	var record = grid.getSelectionModel().selected;
	if(record.length == 0){
		msgTishi('请选择要查看明细的项！');
		return;
	}
	initLoanGuarcreditlimsgmtFormDetail();
	loanGuarcreditlimsgmtWinDetail = Ext.create('Ext.Window',{
		layout:'fit',
		width:800,
		height:400,
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'明细信息',
		listeners:{
			minimize:function(win,opts){
				if(!win.collapse()){
					win.collapse();
				}else{
					win.expand();
				}
			}
		},
		items:loanGuarcreditlimsgmtFormDetail,
		buttons:[{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	loanGuarcreditlimsgmtWinDetail.show();
	
	loadFormData(loanGuarcreditlimsgmtFormDetail,'../loanGuarcreditlimsgmtController/getLoanGuarcreditlimsgmtById?loan_guarcreditlimsgmt_id='+ record.items[0].data.loan_guarcreditlimsgmt_id);
}
