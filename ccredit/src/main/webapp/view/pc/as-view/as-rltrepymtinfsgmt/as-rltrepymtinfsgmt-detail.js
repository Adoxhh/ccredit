var asRltrepymtinfsgmtWinDetail;
var asRltrepymtinfsgmtFormDetail;
function detailAsRltrepymtinfsgmt(){
	var record = grid.getSelectionModel().selected;
	if(record.length == 0){
		msgTishi('请选择要查看明细的项！');
		return;
	}
	initAsRltrepymtinfsgmtFormDetail();
	
	var selected=record.items[0].data.infoidtype;
	if(selected=='1'){	         
		Ext.getCmp('arlpcerttype').setStore(IDTYPECODE);
	}
	if(selected=='2'){	         
		Ext.getCmp('arlpcerttype').setStore(OTHENTCERTTYPECODE);
	}
	asRltrepymtinfsgmtWinDetail = Ext.create('Ext.Window',{
		layout:'fit',
		width:800,
		height:400,
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'明细信息',
		listeners:{
			minimize:function(win,opts){
				if(!win.collapse()){
					win.collapse();
				}else{
					win.expand();
				}
			}
		},
		items:asRltrepymtinfsgmtFormDetail,
		buttons:[{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	asRltrepymtinfsgmtWinDetail.show();
	
	loadFormData(asRltrepymtinfsgmtFormDetail,'../asRltrepymtinfsgmtController/getAsRltrepymtinfsgmtById?rltrepymtinfsgmt_id='+ record.items[0].data.rltrepymtinfsgmt_id);
}
